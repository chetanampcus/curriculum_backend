CREATE TABLE organizations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   created_by VARCHAR(255),
   created_date TIMESTAMP WITHOUT TIME ZONE,
   last_modified_by VARCHAR(255),
   last_modified_date TIMESTAMP WITHOUT TIME ZONE,
   entity_name VARCHAR(255) NOT NULL,
   entity_type VARCHAR(255),
   entity_level VARCHAR(255),
   program_type VARCHAR(255),
   contact_name VARCHAR(255),
   primary_email VARCHAR(255),
   primary_phone VARCHAR(255),
   complete_phone_number VARCHAR(255),
   address1 VARCHAR(255),
   address2 VARCHAR(255),
   city VARCHAR(255),
   state_province VARCHAR(255),
   zip_code VARCHAR(255),
   no_of_licenses INTEGER,
   subscription_type VARCHAR(255),
   subscription_start_date TIMESTAMP WITHOUT TIME ZONE,
   subscription_expired_date TIMESTAMP WITHOUT TIME ZONE,
   hs_uuid UUID,
   hs_id INTEGER,
   hs_unique_id VARCHAR(255),
   is_deleted BOOLEAN,
   country_code VARCHAR(255),
   label VARCHAR(255),
   value VARCHAR(255),
   CONSTRAINT pk_organizations PRIMARY KEY (id)
);